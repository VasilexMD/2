// Программа, которая вычисляет 10% от введенного пользователем числа:

let num = prompt("Введите число:");
let tenPercent = num * 0.1;
console.log("10% от введенного числа равно: " + tenPercent);

// Программа, которая сравнивает два числа и выводит наименьшее из них:

let num1 = prompt("Введите первое число:");
let num2 = prompt("Введите второе число:");
if (num1 < num2) {
  console.log(num1 + " меньше, чем " + num2);
} else {
  console.log(num2 + " меньше, чем " + num1);
}

// Программа, которая выводит сообщение в зависимости от введенного числа:

let num = prompt("Введите число:");
if (num < 100) {
  console.log("Введенное число меньше 100.");
} else if (num > 100) {
  console.log("Введенное число больше 100.");
} else {
  console.log("Введенное число равно 100.");
}
 
